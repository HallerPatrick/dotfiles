local abv_to_val = {
  Monday = 0,
  Tuesday = 1,
  Wednesay = 2,
  Thursday = 3,
  Friday = 4,
  Saturday = 5,
  Sunday = 6
}

local function next_monday()
  local current_day = os.date("%A")
  local day_val = abv_to_val[current_day]
  local diff = 7 - day_val

  if diff == 0 then
    return { 
      "",
      "",
      "",
      "",
      "",
      "",
      "... and today is Monday"
    } 
  else
    return {
      "",
      "",
      "",
      "",
      "",
      "",
      "... and in " .. diff .. " days it is Monday again"
    }
  end
end


